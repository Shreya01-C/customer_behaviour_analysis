-- 8. Top 5 products with the highest discount usage %
Select item_purchased, count(item_purchased) as total_number_of_times_sold, count(case when discount_applied = "YES" then 1 end) as number_of_times_discount_applied,
count(case when discount_applied = "YES" then 1 end)*100/count(*) as discount_percent
from customer_behavior_analysis
group by item_purchased
order by discount_percent desc
limit 5;

-- BUSINESS PROBLEM : Some products maybe overly dependent on discount
-- IMPACT :
		   -- Identifies discount-driven products
           -- Help optimize pricing sstrategy
           -- Reduces profit margin loss
           
-- 9. Segment customers into new, returning and loyal based on their total numberr of previous purhases and show the count of each segment
Select case 
           when previous_purchases = '0' then 'New Customer'
           when previous_purchases between 1 and 15 then 'Returning Customer'
       else 'Loyal Customer'
end as customer_segment,
count(*) as customer_count
from customer_behavior_analysis
group by customer_segment;

-- BUSINESS PROBLEM : Lack of customer segmentatiion leads to generic strategies
-- IMPACT :
		   -- Enables personalized marketing
           -- Increase conversion rates
           -- Improves retention strategies
           
-- 10. Top 3 most purchased products within each category
with cte as(
Select category, item_purchased, count(item_purchased) as No_of_purchases,
rank() over(partition by category order by count(item_purchased) desc) as rnk
from customer_behavior_analysis
group by category, item_purchased)
SELECT * from cte
where rnk <= 3;

-- BUSINESS PROBLEM : SCompany doesn't know about top-performing products in each category 
-- IMPACT :
		   -- Improves product placement and recommendations
           -- Helps inverntory optimization
           -- Increases sales through best-sellers
           
-- 11. Are customers who are repeat buyers (more than 5 previous purchases) also likely to subscibe?
Select case
           when previous_purchases > 5 then 'Repeat Buyers'
	   else 'Normal Buyers'
       end as customer_type, subscription_status, count(*) as customer_count
from customer_behavior_analysis
group by customer_type, subscription_status;

-- BUSINESS PROBLEM : Unclear realtionship between loyalty and subscription
-- IMPACT :
		   -- Improves subscription targeting
           -- Increases conversion tp paid progress
           -- Increase customer retention strategy
           
-- 12. What is the revenue contribution of each group
Select case
           when age between 18 and 25 then '18-25'
           when age between 26 and 35 then '26-35'
           when age between 36 and 50 then '36-50'
           else '51+'
		end as age_group, round(sum(purchase_amount),2) as total_revenue
from customer_behavior_analysis
group by age_group
order by total_revenue desc;

