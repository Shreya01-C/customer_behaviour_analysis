-- 1. Which category generate the highest revenue?
Select category, round(sum(purchase_amount)) as highest_revenue
from customer_behavior_analysis
group by category
order by highest_revenue DESC;

--  BUSINESS PROBLEM - Company doesn't know which category contributes the most to the revenue.
-- IMPACT : 
           -- Helps prioritize high-performing categories
           -- Optimizes inventory planning
           -- Improves marketing ROI
           
 -- 2. Are discounts profitable?
 Select discount_applied, round(sum(purchase_amount)) as total_revenue, round(avg(purchase_amount)) as avg_revenue
 from customer_behavior_analysis
 group by discount_applied;
 
-- BUSINESS PROBLEM : Discounts may reduce profit without increasing sale
-- IMPACT :
		   -- Identify effectiveness of discounts
           -- Reduce unnecessary discount costs
           -- Improve profit margins
           
-- 3. What is the total revenue generated by males and females customers? 
Select gender, round(sum(purchase_amount)) as revenue
from customer_behavior_analysis
group by gender;

-- BUSINESS PROBLEM : Lack of understanding revenue segmentation among genders
-- IMPACT :
		   -- Helps design targeted marketed campaign
           -- Improves customer segmenattion strategies
           -- Enhances personalization efforts
           
-- 4. Which customer used a discount but still spent more than the average purchase amount? 
Select customer_id, purchase_amount, discount_applied
from customer_behavior_analysis
where discount_applied = "Yes" AND purchase_amount > (Select round(avg(purchase_amount)) 
                         from customer_behavior_analysis)
order by purchase_amount desc
limit 10;

-- BUSINESS PROBLEM : Company cannot identify high spending customers who are also the discount users
-- IMPACT :
		   -- Identifies premium discount-sensistive customers
           -- Enables targeted discount campaigns
           -- Improves customer retention and revenue
           
-- 5. Which are the top/bottom 5 products with the highest average review rating? 
Select item_purchased, round(avg(review_rating)) as avg_ratings
from customer_behavior_analysis
group by item_purchased
order by avg_ratings desc
limit 5;

Select item_purchased, round(avg(review_rating)) as avg_ratings
from customer_behavior_analysis
group by item_purchased
order by avg_ratings asc
limit 5;

-- BUSINESS PROBLEM : No visibility into product performance based on customer validation
-- IMPACT :
		   -- Promotes high-performing products
           -- Improves low rated products
           -- Enhances customer experience
           
-- 6. Avergae purchase : Standard vs Express Shipping 
Select shipping_type, count(distinct customer_id) as order_placed,round(sum(purchase_amount), 2) as revenue ,round(avg(purchase_amount), 2) as avg_ratings
from customer_behavior_analysis
where shipping_type in ('Standard', 'Express')
group by shipping_type
order by revenue desc;

-- BUSINESS PROBLEM : Unclear if faster shipping leads to higher spending
-- IMPACT :
		   -- Helps optimize shipping pricing strategy
           -- Encourage premium shipping adoption
           -- Increase average order value
           
